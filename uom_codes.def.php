class uom_code {
    // Get the measure from an uom code.
    // @param: $uom_code (String)
    static public function get_measure_from_uom_code($uom_code) {
        $uom_codes = self::define_uom_codes();
        return array_key_exists($uom_code,$uom_codes) ? $uom_codes[$uom_code] : false;
    }

    // Get the uom code from a measure.
    // @param: $measure (String)
    static public function get_uom_code_from_measure($measure) {
        $uom_codes = array_flip(self::define_uom_codes());
        return array_key_exists($measure,$uom_codes) ? $uom_codes[$measure] : false;
    }

    // Returns the lookup array
    static public function get_lookup_array() {
        return self::define_uom_codes();
    }

